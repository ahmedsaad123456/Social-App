class LikeModel {
  String? name;
  String? uId;
  String? image;


//================================================================================================================================

  // Constructor for like model

  LikeModel({
    this.name,
    this.uId,
    this.image,
  });

//================================================================================================================================

  //Named Constructor to convert JSON data to like model object

  LikeModel.fromJson(Map<String, dynamic>? json) {
    name = json!['name'];
    uId = json['uId'];
    image = json['image'];

  }

//================================================================================================================================


  // Convert like model object to a json object

  Map<String, dynamic> toMap() {
    return {
      'name': name,
      'uId': uId,
      'image': image,
    };
  }

//================================================================================================================================

}

