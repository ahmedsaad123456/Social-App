class CommentModel {
  String? name;
  String? uId;
  String? image;
  String? text;
  String? dateTime;

//================================================================================================================================


  CommentModel({
    this.name,
    this.uId,
    this.image,
    this.text,
    this.dateTime,
  });

//================================================================================================================================


  CommentModel.fromJson(Map<String, dynamic>? json) {
    name = json!['name'];
    uId = json['uId'];
    image = json['image'];
    text = json['text'];
    dateTime = json['dateTime'];
  }

//================================================================================================================================


  Map<String, dynamic> toMap() {
    return {
      'name': name,
      'uId': uId,
      'image': image,
      'text': text,
      'dateTime': dateTime,
    };
  }

//================================================================================================================================

}

